class Cube {
  private int size;
  private int color;
  private int index;

  public Cube(int size, int color, int index) {
    this.size = size;
    this.color = color;
    this.index = index;
  }

  public int getSize() {
    return size;
  }

  public int getColor() {
    return color;
  }

  public int getIndex() {
    return index;
  }
}
